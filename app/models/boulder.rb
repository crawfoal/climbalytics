class Boulder < ActiveRecord::Base

  #-----------------------------------------------------------------------------
  # Pictures
  #-----------------------------------------------------------------------------
  mount_uploader :picture, PictureUploader

  #-----------------------------------------------------------------------------
  # Grades
  #-----------------------------------------------------------------------------
  enum grade: (["VB"] + ((0..16).to_a.map {|num| "V#{num}"}))

  #-----------------------------------------------------------------------------
  # Setter
  #-----------------------------------------------------------------------------
  belongs_to :setter_story

  def setter
    setter_story.user if setter_story
  end

end
